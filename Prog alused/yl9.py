AB = float(input("Sisestage kolmnurga AB külg: "))
AC = float(input("Sisestage kolmnurga AC külg: "))
BC = float(input("Sisestage kolmnurga BC külg: "))
if AC == BC and BC == AB :
    print("Kolmnurk on võrdkülgne")
elif AC == BC:
    print("Kolmnurk on võrdhaarne")
else:
    print ("Kolmnurk on erikülgne")
    



